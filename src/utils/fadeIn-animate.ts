export const fadeInAnimate = (index: number) => {
  switch (index) {
    case 0:
      return ' animate-show0';
    case 1:
      return ' animate-show1';
    case 2:
      return ' animate-show2';
    case 3:
      return ' animate-show3';
    case 4:
      return ' animate-show4';
    case 5:
      return ' animate-show5';
  }
};
